    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        id = txt_id.getText();
        ruangan = txt_ruangan.getText();

        try {
            sql = "INSERT INTO t_ruangan"
                    + "(id_ruangan, nama_ruangan ) VALUES "
                    + "('" + id + "',"
                    + "'" + ruangan + "')";

            st = con.createStatement();
            st.execute(sql);
            tampilruangan("SELECT * FROM t_ruangan");
            JOptionPane.showMessageDialog(rootPane, "Selamat Anda Berhasil Menyimpan Data Ruangan");
            btnsimpan.setEnabled(false);
            btntambah.setEnabled(true);
            kosongkan();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Data Gagal Disimpan Mohon Periksa Kembali Codingnya" + e.getMessage());
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

